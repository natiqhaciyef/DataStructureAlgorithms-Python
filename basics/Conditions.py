# CONDITION
temperature = -1
if temperature > 30:
    print(f"The {temperature} is hot")
elif temperature > 20:
    print(f"The {temperature} is warm")
elif temperature > 15:
    print(f"The {temperature} is cool")
elif temperature > 0:
    print(f"The {temperature} is cold")
else:
    print(f"The {temperature} is freezing")